// 展开所有小阿卡那牌为一维数组
export const getAllMinorArcana = () => {
  const allCards = []
  for (const suit of Object.values(minorArcana)) {
    allCards.push(...suit.suits)
  }
  return allCards
}

// 获取所有78张牌
export const getAllCards = () => {
  const major = majorArcana.map(card => ({ ...card, type: "major" }))
  const minor = getAllMinorArcana().map(card => ({ 
    ...card, 
    type: "minor", 
    suit: card.id[0] === "W" ? "wands" : card.id[0] === "C" ? "cups" : card.id[0] === "S" ? "swords" : "pentacles" 
  }))
  return [...major, ...minor]
}

